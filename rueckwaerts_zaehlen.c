#include <stdio.h>

int main() {
    int i = 10;

while (i >= 0) {
        printf("%d\n", i);
        i--;
    }
    
    return 0;
}
// This program counts down from 10 to 1 and prints each number on a new line.
// It uses a while loop to decrement the value of i until it reaches 1.
// The loop continues as long as i is greater than or equal to 1.
// The printf function is used to print the current value of i in each iteration.
// The program ends with a return statement, indicating successful completion.       